class CircularQueue
  attr_reader :queue, :buffer_size

  def initialize(buffer_size)
    @queue = []
    @buffer_size = buffer_size
  end

  def enqueue(object)
    dequeue if queue_full?
    queue << object
  end

  def dequeue
    queue.shift
  end

  def queue_full?
    queue.size == buffer_size
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
queue.enqueue(2)
puts queue.dequeue == 1

queue.enqueue(3)
queue.enqueue(4)
puts queue.dequeue == 2

queue.enqueue(5)
queue.enqueue(6)
queue.enqueue(7)
puts queue.dequeue == 5
puts queue.dequeue == 6
puts queue.dequeue == 7
puts queue.dequeue == nil

